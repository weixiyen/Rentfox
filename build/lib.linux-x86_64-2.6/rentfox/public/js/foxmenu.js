function Foxmenu() 
{ 
	var submenu = $("#menu .sub")
	var backdrop = $("#menuBackdrop")
	
	/* method definitions
	---------------------------------*/
	this.hideSubmenu = hideSubmenu
	this.showSubmenu = showSubmenu

	
	/* methods
	---------------------------------*/
	function hideSubmenu() {
		submenu.hide()
		backdrop.removeClass("submenu")
	}

	function showSubmenu() {
		submenu.show()
		backdrop.addClass("submenu")
	}
	
}